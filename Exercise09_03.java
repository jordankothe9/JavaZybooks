public class Exercise09_03 {
    public static void main(String[] args) {
        Date time = new Date();
        
        
        time.setTime(10000L);
        System.out.println(time);
        time.setTime(100000L);
        System.out.println(time);
        time.setTime(1000000L);
        System.out.println(time);
        time.setTime(10000000L);
        System.out.println(time);
        time.setTime(100000000L);
        System.out.println(time);
        time.setTime(1000000000L);
        System.out.println(time);
        time.setTime(10000000000L);
        System.out.println(time);
        time.setTime(100000000000L);
        System.out.println(time);
        time.setTime(1000000000000L);
        System.out.println(time);
    }
}
